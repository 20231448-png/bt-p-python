e = int(input("Nhap so e: "))
f = int(input("Nhap so f: "))
g = int(input("Nhap so g: "))

# tong va tich
tong = e + f + g
tich = e * f * g

# hieu cua 2 so
hieu = e - f

# phep chia
chia = e / f
phan_nguyen = e // f
phan_du = e % f

print("Tong 3 so:", tong)
print("Tich 3 so:", tich)

print("Hieu e - f:", hieu)

print("Ket qua chia e / f:", chia)
print("Phan nguyen:", phan_nguyen)
print("Phan du:", phan_du)